<?php 

$a = 10;
$b = 2;


echo $a + $b;
// + é soma
echo "<br>";
// "<br>" é quebra de linha
echo $a - $b;
//- é subtração
echo "<br>";

echo $a * $b;
//* é vezes
echo "<br>";

echo $a / $b;
//"/" é divisão
echo "<br>";

echo $a % $b;
// %(tambem conhecido como modulo) é resto de divisão 
echo "<br>";

echo $a ** $b;
//** é exponenciação, neste caso 10 elevado ao quadrado
?>